function set_theme()
    term.setPaletteColor(colors.black, 0x181825)
    term.setPaletteColor(colors.gray, 0x1e1e2e)
    term.setPaletteColor(colors.lightGray, 0x9399b2)
    term.setPaletteColor(colors.colors.white, 0xcdd6f4)
    term.setPaletteColor(colors.red, 0xf38ba8)
    term.setPaletteColor(colors.purple, 0xcba6f7)
end